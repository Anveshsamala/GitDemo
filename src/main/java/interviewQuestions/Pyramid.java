package interviewQuestions;

public class Pyramid {

	public static void main(String[] args) {
		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= i; j++) {
				System.out.print(j);
			}
			System.out.println("");
		}

		System.out.println(
				"In new branch i am added this code in pyramid class interviewquestions package and gitdemo branch project");

	}

}
